export async function prepareFixture({
  fixtureName,
  providedRepoPath,
  repoDir,
  fixtureDir,
}: PrepareFixtureArgs) {
  await rm(repoDir, { recursive: true, force: true });
  await rm(fixtureDir, { recursive: true, force: true });
  await ensureDir(fixtureDir);

  switch (fixtureName) {
    case "clean-scaffold":
      await copyProvidedRepo(providedRepoPath, repoDir);
      return;
    case "greeting-cli-diff":
      await copyProvidedRepo(providedRepoPath, repoDir);
      await writeFiles(repoDir, {
        "index.ts": [
          "const name = Bun.argv[2] ?? \"world\";",
          "",
          "console.log(`Hello, ${name}!`);",
          "",
        ].join("\n"),
        "index.test.ts": [
          "import { expect, test } from \"bun:test\";",
          "import { resolve } from \"node:path\";",
          "",
          "const decoder = new TextDecoder();",
          "const entry = resolve(import.meta.dir, \"index.ts\");",
          "",
          "function runCli(args: string[] = []) {",
          "  return Bun.spawnSync({",
          "    cmd: [\"bun\", entry, ...args],",
          "    stdout: \"pipe\",",
          "    stderr: \"pipe\",",
          "    env: process.env,",
          "  });",
          "}",
          "",
          "test(\"prints default greeting\", () => {",
          "  const result = runCli();",
          "  expect(result.exitCode).toBe(0);",
          "  expect(decoder.decode(result.stdout).trim()).toBe(\"Hello, world!\");",
          "});",
          "",
          "test(\"prints named greeting\", () => {",
          "  const result = runCli([\"Alice\"]);",
          "  expect(result.exitCode).toBe(0);",
          "  expect(decoder.decode(result.stdout).trim()).toBe(\"Hello, Alice!\");",
          "});",
          "",
        ].join("\n"),
        "README.md": [
          "# testing-repo",
          "",
          "To install dependencies:",
          "",
          "```bash",
          "bun install",
          "```",
          "",
          "To run the default greeting:",
          "",
          "```bash",
          "bun run index.ts",
          "```",
          "",
          "To run a named greeting:",
          "",
          "```bash",
          "bun run index.ts Alice",
          "```",
          "",
          "To run tests:",
          "",
          "```bash",
          "bun test",
          "```",
          "",
        ].join("\n"),
      });
      return;
    case "inventory-api-base":
      await buildInventoryBase(repoDir);
      return;
    case "inventory-api-feature-diff":
      await buildInventoryBase(repoDir);
      await applyInventoryFeatureDiff(repoDir);
      return;
    case "inventory-api-review-packet":
      await buildInventoryBase(repoDir);
      await applyInventoryReviewPacket(repoDir, fixtureDir);
      return;
    case "shipments-api-base":
      await buildShipmentsBase(repoDir);
      return;
    case "shipments-api-feature-diff":
      await buildShipmentsBase(repoDir);
      await applyShipmentsFeatureDiff(repoDir);
      return;
    case "shipments-api-review-packet":
      await buildShipmentsBase(repoDir);
      await applyShipmentsReviewPacket(repoDir, fixtureDir);
      return;
  }
}
