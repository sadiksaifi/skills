async function ensureDir(path: string) {
  await mkdir(path, { recursive: true });
}

async function writeText(path: string, content: string) {
  await ensureDir(dirname(path));
  await writeFile(path, content);
}

async function writeFiles(rootDir: string, files: Record<string, string>) {
  await Promise.all(
    Object.entries(files).map(([relativePath, content]) =>
      writeText(join(rootDir, relativePath), content),
    ),
  );
}

async function run(command: string, cwd: string) {
  const proc = Bun.spawn(["zsh", "-lc", command], {
    cwd,
    stdout: "pipe",
    stderr: "pipe",
    env: process.env,
  });
  const [stdout, stderr] = await Promise.all([
    new Response(proc.stdout).text(),
    new Response(proc.stderr).text(),
  ]);
  const exitCode = await proc.exited;

  if (exitCode !== 0) {
    throw new Error(
      [`command failed: ${command}`, `cwd: ${cwd}`, stdout, stderr]
        .filter(Boolean)
        .join("\n"),
    );
  }
}

async function initGitRepo(repoDir: string, message: string) {
  await run("git init -b main", repoDir);
  await run('git config user.name "Snap Evals"', repoDir);
  await run('git config user.email "snap-evals@example.com"', repoDir);
  await run("git add .", repoDir);
  await run(`git commit -m ${JSON.stringify(message)}`, repoDir);
}

async function copyProvidedRepo(providedRepoPath: string, repoDir: string) {
  await rm(repoDir, { recursive: true, force: true });
  await cp(providedRepoPath, repoDir, { recursive: true });
}

